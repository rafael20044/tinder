package io.ionic.starter.model;

public class Photo {
  private String path;
  private String url;

  // --- Getters y Setters ---

  public String getPath() {
    return path;
  }

  public void setPath(String path) {
    this.path = path;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }
}
